public class Nestedifelse1 
{

	public static void main(String[] args)
	{
		
		int a=15;
		int b=90;
		int c=112;
		
		if(a>b)
		{
			if(a>c)
			{
				System.out.println("logic1");
			}
			else
			{
				System.out.println("logic2");
			}
		}
		else
		{
			if(b>c)
			{
				System.out.println("logic3");
			}
			else
			{
				System.out.println("logic4");
			}
		}
		
	}
}

		

